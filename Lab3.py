# Вхідні дані
m_values = [1.2, 1, 1.5]

# Обчислюємо масу борошна для кожного значення m
for m in m_values:
    # Маса яблук (60% від загальної маси)
    apples_mass = 0.6 * m
    
    # Маса тіста (40% від загальної маси)
    dough_mass = 0.4 * m
    
    # Маса борошна в тісті (70% від маси тіста)
    flour_mass = 0.7 * dough_mass
    
    # Переводимо в грами
    flour_mass_in_grams = flour_mass * 1000
    
    # Виводимо результат
    print(f"Для маси пирога {m} кг маса борошна в пирозі: {flour_mass_in_grams} грамів.")
